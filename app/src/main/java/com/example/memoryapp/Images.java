package com.example.memoryapp;

public class Images {
    int images;
    int key;

    public Images(int images, int key) {
        this.images = images;
        this.key = key;
    }

    public int getImages() {
        return images;
    }

    public void setImages(int images) {
        this.images = images;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }
}
